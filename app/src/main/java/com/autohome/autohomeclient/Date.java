public class Date {

	int year;
	int month;
	int week;
	int hour;
	int min;

	public Date(int year, int month, int week, int hour, int min) {
		this.year = year;
		this.month = month;
		this.week = week;
		this.hour = hour;
		this.min = min;
	}
}
